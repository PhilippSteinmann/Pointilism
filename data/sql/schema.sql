CREATE TABLE image_keyword_map (id BIGINT AUTO_INCREMENT, image_id BIGINT, keyword_id BIGINT, PRIMARY KEY(id)) ENGINE = INNODB;
CREATE TABLE images (id BIGINT AUTO_INCREMENT, url VARCHAR(255), color VARCHAR(20), PRIMARY KEY(id)) ENGINE = INNODB;
CREATE TABLE keywords (id BIGINT AUTO_INCREMENT, keyword VARCHAR(100), PRIMARY KEY(id)) ENGINE = INNODB;
